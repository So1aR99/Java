class Cat
{
    String name;
    int age;
    String color;
    Cat(){
    }
    Cat(String name, int age){
        this.name = name;
        this.age = age;
    }
    Cat(String name, int age, String color){
        this.name = name;
        this.age = age;
        this.color = color;
    }
    public void Meow(){
        System.out.println("야옹");
    }
    public void Eat(){
        System.out.println("냠냠");
    }
}
public class Main {
    public static void main(String[] args) {
        Cat dori = new Cat();
        dori.Meow();
        Cat ruby = new Cat("루비", 4);
        System.out.println("고양이 이름 : " + ruby.name);
        System.out.println("고양이 나이 : " + ruby.age);
        ruby.Eat();
        System.out.println();
        Cat pi = new Cat("파이", 6, "검은색");
        System.out.println("고양이 이름 : " + pi.name);
        System.out.println("고양이 나이 : " + pi.age);
        System.out.println("고양이 색깔 : " + pi.color);
        pi.Eat();
        pi.Meow();
    }
}
